        JSON or JavaScript Object Notation is open-source portable data format.<br>
        <br>
        JSON is used primarily to transmit data between a server and web application, as an alternative to XML and makes is easy to transform your data into what ever for you want; XML, XLS, SQL, HTML.<br>
        <br>
        JSON is described by standards, RFC 7159 and ECMA-404.
